

public class Division
{
   public Division()
   {
      
   }
   
   public static int intDivide(int numIn, int denomIn)
   {
      int num = numIn;
      int denom = denomIn;
      
      return num/denom;
   }
   
   public static double decimalDivide(int numIn, int denomIn)
   {
      double num = numIn;
      double denom = denomIn;
      return num/denom;
   }
   
   

}